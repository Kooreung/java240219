package c02.lecture.p02type;

public class C03Integer {
    public static void main(String[] args) {

        // 정수표현방법 : 10진법
        int a = 32;
        int b = 23_353;

        // 정수표현방법 : 2진법 (0, 1)
        int c = 0b01001101;

        // 정수표현방법 : 8진법 (0 ~ 7)
        int d = 027123;

        // 정수표현방법 : 16진법 (0 ~ 9 , A ~ F)
        int e = 0x82ace2;


    }
}
