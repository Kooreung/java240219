package ch04.lecture.p4for;

public class C04ForExercise {
    public static void main(String[] args) {
        for (int i = 0; i < 10 ; i +=2) {
            System.out.println(i);
        }
        System.out.println("-----------");
        for (int i = 2; i < 12 ; i +=2) {
            System.out.println(i);
        }
        System.out.println("-----------");
        for (int i = 4; i >= 0 ; i --) {
            System.out.println(i);
        }
        System.out.println("-----------");

        for (int i = 5; i > 0 ; i --) {
            System.out.println(i);
        }
    }
}
