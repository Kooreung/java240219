package ch06.exercise.p16;

public class Printer {
    public void println(int printInt) {
        System.out.println(printInt);
    }
    public void println(boolean printBoolean) {
        System.out.println(printBoolean);
    }

    public void println(double printDouble) {
        System.out.println(printDouble);
    }

    public void println(String printString) {
        System.out.println(printString);
    }
}
